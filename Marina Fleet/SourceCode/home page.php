    var images = ["img/a.jpg", "img/b.jpg", "img/agt0.jpg", "img/agt1.jpg", "img/eat.jpg", "img/bnn.png"];
    // Thời gian hiển thị của mỗi ảnh (đơn vị: giây)
    var interval = 3;
    var index = 0;
    // Hàm thay đổi ảnh
    function changeImage() {
      var img = document.getElementById("anhGioiThieu");
      img.src = images[index];
      index = (index + 1) % images.length;
